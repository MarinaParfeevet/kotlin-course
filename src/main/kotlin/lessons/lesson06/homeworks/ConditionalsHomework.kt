        "png", "gif", "jpeg", "svg", "bmp" -> println("изображения")
        "xlsx", "xls", "ods", "csv" -> println("таблица")
        else -> println("неизвестный тип")
    }
}

//Задание 6: "Конвертация температуры"
//Создайте функцию, которая конвертирует температуру из градусов Цельсия в Фаренгейты и наоборот в зависимости от
// указанной единицы измерения (C/F). Единицу измерения нужно передать вторым аргументом функции. Несколько аргументов
// передаются через запятую. Распечатай в консоль результат конвертации с добавлением единицы измерения. Чтобы добавить
// единицу измерения после результата используй функцию печати без переноса строки print("C") или print("F").
fun convertTemperature(temp: Double, ind: Char) {
    var t = temp
    if (ind == 'F') {
        t = 5 / 9 * (temp - 32)
        print(t)
        println("C")
    } else if (ind == 'C') {
        t = temp * 9 / 5 + 32
        print(t)
        println("F")
    } else println("wrong input data")
}
//Задание 7: "Подбор одежды по погоде"
//Напишите функцию, которая на основе температуры воздуха рекомендует тип одежды: "куртка и шапка" при температуре
// ниже +10, "ветровка" от +10 до +18 градусов включительно и "футболка и шорты" при температуре выше +18 градусов.
// При температурах ниже -30 и выше +35 рекомендуйте не выходить из дома.
fun chooseClothes(temp:Int): String{
    when (temp) {
        in -30..9 -> return "куртка и шапка"
        in 10..18 -> return "ветровка"
        in 19..35 ->return "футболка и шорты"
        else -> return "сиди дома и не высовывайся"
    }
}
//Задание 8: "Выбор фильма по возрасту"
//Кинотеатр предлагает фильмы разных возрастных категорий. Напишите функцию, которая принимает возраст зрителя и
// возвращает доступные для него категории фильмов: "детские" (от 0 до 9), "подростковые" (от 10 до 18), "18+" для
// остальных.
fun chooseFilmByAge(age:Int): String{
    when (age) {
        in 0..9 -> return "детские"
        in 10..18 -> return "подростковые"
        else -> return "18+"
    }
}
