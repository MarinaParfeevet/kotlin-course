fun findBestTransport(distance: Double) {
    when (distance) {
        in 0.0..<1.0 -> println("on foot")
        in 1.0..5.0 -> println("by bicycle")
        else -> println("use transport")
    }
}

//Задание 4: "Расчет бонусных баллов"
//Клиенты интернет-магазина получают бонусные баллы за покупки. Напишите функцию, которая принимает сумму покупки и
// печатает в консоль количество бонусных баллов: 2 балла за каждые 100 рублей при сумме покупки до 1000 рублей и 3 балла
// за каждые 100 рублей при сумме свыше этого.
fun findBonus(sum: Int) {
    when (sum) {
        in 0..1000 -> println((sum / 100) * 2)
        else -> println((sum / 100) * 3)
    }
}

//Задание 5: "Определение типа документа"
