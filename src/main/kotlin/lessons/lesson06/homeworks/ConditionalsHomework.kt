        4 -> println(Month.APRIL)
        5 -> println(Month.MAY)
        6 -> println(Month.JUNE)
        7 -> println(Month.JULY)
        8 -> println(Month.AUGUST)
        9 -> println(Month.SEPTEMBER)
        10 -> println(Month.OCTOBER)
        11 -> println(Month.NOVEMBER)
        12 -> println(Month.DECEMBER)
        else -> println("error")
    }
}

//Задание 2: "Расчет возраста питомца"
//Создайте функцию, которая преобразует возраст собаки в "человеческие" годы. До 2 лет каждый год собаки
// равен 10.5 человеческим годам, после - каждый год равен 4 человеческим годам. Результат распечатай в консоль.
fun petAge(petAge: Int): Double {
    return if (petAge in 0..2) petAge * 10.5
    else petAge * 4.0
}

//Задание 3: "Определение способа перемещения"
